private const val ARG_PARAM1 = "param1"
private const val ARG_PARAM2 = "param2"

/**
 * A simple [Fragment] subclass.
 * Use the [FragmentB.newInstance] factory method to
 * create an instance of this fragment.
 */
class FragmentB : Fragment() {
    // TODO: Rename and change types of parameters
    private var param1: String? = null
    private var param2: String? = null

    var uri: Uri? =null
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        arguments?.let {
            param1 = it.getString(ARG_PARAM1)
            param2 = it.getString(ARG_PARAM2)
        }
    }

    override fun onCreateView(
        inflater: LayoutInflater, container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View? {
        // Inflate the layout for this fragment
        val view = inflater.inflate(R.layout.fragment_b, container, false)
        view.galleryButton.setOnClickListener {
            getFromAlbum()
        }
        return view
    }

    fun getFromAlbum(){
        val intent = Intent("android.intent.action.GET_CONTENT")
        intent.type="image/*"
        startActivityForResult(intent, 102)
    }

    override fun onActivityResult(requestCode: Int, resultCode: Int, data: Intent?) {
        super.onActivityResult(requestCode, resultCode, data)
        when(requestCode){
            102 -> {
                if (resultCode == Activity.RESULT_OK){
                    if (Build.VERSION.SDK_INT >= 19){
                        uri = data?.data
                        if(uri != null){
                            val bitmap = BitmapFactory.decodeStream(requireActivity().contentResolver.openInputStream(uri!!))
                            output3.setImageBitmap(bitmap)
                        }
                    }

                }
            }
        }
    }
    companion object {
        /**
